public class ChooseOfThree {
    String one;
    String two;
    String three;

    public ChooseOfThree(String first, String second, String third) {
        this.one = first;
        this.two = second;
        this.three = third;
    }

    public String getOne() {
        return one;
    }

    public String getTwo() {
        return two;
    }

    public String getThree() {
        return three;
    }
}
